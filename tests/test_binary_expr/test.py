# Modulo operator with numbers
print(10 % 3)
print(7 % 2)
print(15 % 5)

# Modulo operator with variables
x = 21
y = 8
print(x % y)

# Modulo operator in if statement
number = 17
if number % 2 == 0:
    print("Number is even")
else:
    print("Number is odd")

# Modulo operator in while loop
count = 0
while count < 3:
    print(count)
    if count % 2 == 0:
        print("is even")
    else:
        print("is odd")
    count = count + 1

